/*
 Backtracking para gerar todas as sequencias validas de n pares de '<' e '>'.

 A ideia central:
 - Rastreia quantos '<' (open) e '>' (close) ja foram colocados
 - A cada passo, tenta adicionar '<' ou '>'
 - Adiciona '<' se ainda nao colocou todos os n pares de abertura (open < n)
 - Adiciona '>' somente se ha um '<' sem par correspondente (close < open)
 - Quando a string tem n*2 caracteres, esta completa e e adicionada ao resultado

 Exemplo para n=2:
 backtrack("", 0, 0)
   → add '<': backtrack("<", 1, 0)
       → add '<': backtrack("<<", 2, 0)
           → nao pode add '<' (open == n)
           → add '>': backtrack("<<>", 2, 1)
               → add '>': backtrack("<<>>", 2, 2) ✓ completo
       → add '>': backtrack("<>", 1, 1)
           → add '<': backtrack("<><", 2, 1)
               → add '>': backtrack("<><>", 2, 2) ✓ completo
           → nao pode add '>' (close == open)

 Resultado: ["<<>>", "<><>"]
 */
fileprivate func aiSolution(n: Int) -> [String] {
    guard n > 0 else { return [] }

    var result = [String]()

    func backtrack(current: String, open: Int, close: Int) {
        // Sequencia completa
        if current.count == n * 2 {
            result.append(current)
            return
        }

        // Ainda pode abrir '<'
        if open < n {
            backtrack(current: current + "<", open: open + 1, close: close)
        }

        // Pode fechar '>' quando ha '<' sem par
        if close < open {
            backtrack(current: current + ">", open: open, close: close + 1)
        }
    }

    backtrack(current: "", open: 0, close: 0)
    return result
}
